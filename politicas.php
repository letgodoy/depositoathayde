<h3 style="padding-top: 0px; margin-top: 0px;">Regras para trocas e devoluções</h3>
<br>
<br>
<p style="font-size: large;font-weight: bold;">
Em caso de insatisfação:
</p>
<p>
De acordo com os artigos 18 e 26 do Código de Defesa do Consumidor (CDC), nenhuma loja é obrigada a trocar uma mercadoria porque o cliente não gostou, o tamanho não serviu, a cor não agradou, ou porque o produto comprado (ou ganho), não era bem o que o consumidor queria. As trocas serão obrigatórias em caso de defeito do produto.
Para comodidade de nossos clientes realizaremos a troca por insatisfação em até 7 (sete) dias após a compra mediante apresentação da nota entregue no ato da compra. O produto deve estar lacrado e em perfeitas condições de uso. A troca de produtos caracterizados como consumíveis não será realizada a troca. Como por exemplo: brocas, discos de corte e desbaste, ponteiras, tintas, vernizes e etc.
​​</p>
<p style="font-size: large;font-weight: bold;">
Em caso de defeito:
</p>
<p>
Para produtos que apresentem defeito de fabricação, o consumidor deverá entrar em contato diretamente com o fabricante. Algumas marcas de produtos podem ser trocas na loja. Para que isso ocorra, o cliente deverá trazer o produto com defeito, ele será avaliado e dependendo do produto e marca será realizada a troca. O prazo para a troca por defeito é de 30 (trinta) dias após a compra mediante apresentação da nota. No caso de mal uso, o consumidor não tem direito a garantia e nem a troca do produto.
​</p>
<p>
<span style="font-size: large;font-weight: bold;">Arrependimento da Compra:</span>
</p>
<p>
O consumidor tem direito de se arrepender da compra ou contratação de um serviço, devendo inclusive ser ressarcido de valores que eventualmente tenham sido pagos, e desde que a compra tenha ocorrido fora do estabelecimento comercial. Por exemplo: compras por telefone, internet, reembolso postal, etc. O consumidor tem o prazo de 7 (sete) dias a contar da data da assinatura do contrato ou do ato de recebimento do produto para exercer o seu direito de arrependimento, independentemente da qualidade desse. O Código de defesa do consumidor não exige que seja feita a devolução em espécie. Portanto para a devolução de produtos que se enquadrem dentro das regras de Troca por Insatisfação será gerado um haver na loja sem prazo de validade.
</p>
<br>
<p style="font-size: large;font-weight: bold;">
Não é possível o arrependimento da compra de um produto efetuada dentro do estabelecimento comercial. Artigo 49 e § único do Código de Defesa do Consumidor (CDC).
</p>
